    private void submitUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitUserActionPerformed
        int jabatan;
        if (noMenu == 5) {
            jabatan = 1;
        } else {
            jabatan = 3;
        }
        String query = "INSERT INTO `user`(`namaUser`, `username`, `password`, `jabatan`) VALUES ('" + namaPegawai.getText() + "','" + username.getText() + "','" + password.getText() + "','"+jabatan+"')";
        if (con.insertUpdateDelete(query)) {
            JOptionPane.showMessageDialog(panelHome, "Submit Data was Successfull", "Pemberitahuan",1);
            menu(noMenu);
            namaPegawai.setText("");
            username.setText("");
            password.setText("");
            
        }else{
            JOptionPane.showMessageDialog(panelHome, "Submit Data was Failed", "Pemberitahuan",2);
        }
    }//GEN-LAST:event_submitUserActionPerformed

