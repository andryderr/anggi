    public void button(int i) {
        if (simpanButton == i) {
            panelData.setVisible(false);
            panelUser.setVisible(false);
            panelLaporan.setVisible(false);
            simpanButton = 0;
        } else {
            switch (i) {
                case 1:
                    panelData.setVisible(true);
                    panelUser.setVisible(false);
                    panelLaporan.setVisible(false);
                    break;
                case 2:
                    panelData.setVisible(false);
                    panelUser.setVisible(true);
                    panelLaporan.setVisible(false);
                    break;
                case 3:
                    panelData.setVisible(false);
                    panelUser.setVisible(false);
                    panelLaporan.setVisible(true);
                    break;
                case 4:
                    panelData.setVisible(false);
                    panelUser.setVisible(false);
                    panelLaporan.setVisible(false);
                    break;
                default:
                    throw new AssertionError();
            }
            simpanButton = i;
        }
    }

